package id.ac.umn.uas_mobileapp;

public class User {
    private String name;
    private String pass;

    public User() {}

    public void setName(String name) {
        this.name = name;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public String getPass() {
        return pass;
    }

}
